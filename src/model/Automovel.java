package model;

public class Automovel {

    private int aut_aceleracao;
    private int aut_deslocamento;
    private int aut_capacidade;
    private int aut_ano;
    private int aut_portas;

    public int getAut_aceleracao() {
        return aut_aceleracao;
    }

    public void setAut_aceleracao(int aut_aceleracao) {
        this.aut_aceleracao = aut_aceleracao;
    }

    public int getAut_deslocamento() {
        return aut_deslocamento;
    }

    public void setAut_deslocamento(int aut_deslocamento) {
        this.aut_deslocamento = aut_deslocamento;
    }

    public int getAut_capacidade() {
        return aut_capacidade;
    }

    public void setAut_capacidade(int aut_capacidade) {
        this.aut_capacidade = aut_capacidade;
    }

    public int getAut_ano() {
        return aut_ano;
    }

    public void setAut_ano(int aut_ano) {
        this.aut_ano = aut_ano;
    }

    public int getAut_portas() {
        return aut_portas;
    }

    public void setAut_portas(int aut_portas) {
        this.aut_portas = aut_portas;
    }
}
